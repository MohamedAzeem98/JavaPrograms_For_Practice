package GeneralPrograms;

public class ProgramForSwapping {
	public static void main(String[] args) {
		int n1=4,n2=7,temp;
		System.out.println("Before Swapping "+n1+" "+n2);
		temp=n1;
		n1=n2;
		n2=temp;
		
		System.out.println("After Swapping "+n1+" "+n2);
		
		
		
		/*
		 Without third Variable
		 
		 
		  n1=n1+n2;
		 n2=n1-n2;
		 n1=n1-n2; 
		 
		 */
	}

}
